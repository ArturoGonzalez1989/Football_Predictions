"""
Backtest: H103 — BACK Under 3.5 with SoT Dominance
Generated by strategy-designer agent, R19

Concept: When one team dominates shots on target (SoT >= rival + N),
the Under 3.5 market may be mispriced because SoT dominance suggests
controlled attacking (quality over chaos), not necessarily more goals.
The market overreacts to shot pressure by pushing Over odds down.

Unlike under35_late (score 1-0/0-1 required) and under35_3goals (exactly
3 goals required), this strategy uses SoT dominance as the primary trigger
regardless of the specific score.

Anti-tautology: skip if total goals at trigger >= 4 (Under 3.5 already dead).
"""
import os, glob, csv, math, json, itertools
from collections import defaultdict, Counter

DATA_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "betfair_scraper", "data")
STAKE = 10.0

def _f(val):
    try:
        v = float(val)
        return v if not math.isnan(v) else None
    except (TypeError, ValueError):
        return None

def _i(val):
    v = _f(val)
    return int(v) if v is not None else None

def load_matches():
    pattern = os.path.join(DATA_DIR, "partido_*.csv")
    files = glob.glob(pattern)
    matches = []
    for fpath in files:
        rows = []
        try:
            with open(fpath, encoding="utf-8", errors="replace") as f:
                for row in csv.DictReader(f):
                    rows.append(row)
        except Exception:
            continue
        if len(rows) < 5:
            continue
        last = rows[-1]
        gl = _i(last.get("goles_local", ""))
        gv = _i(last.get("goles_visitante", ""))
        if gl is None or gv is None:
            continue
        matches.append({
            "file": os.path.basename(fpath),
            "match_id": os.path.basename(fpath).replace("partido_", "").replace(".csv", ""),
            "country": last.get("Pais", last.get("País", "?")),
            "league": last.get("Liga", "?"),
            "ft_local": gl,
            "ft_visitante": gv,
            "ft_total": gl + gv,
            "rows": rows,
            "timestamp_first": rows[0].get("timestamp_utc", ""),
        })
    return matches

def wilson_ci95(n, wins):
    if n == 0:
        return (0.0, 0.0)
    z = 1.96
    p = wins / n
    denom = 1 + z * z / n
    centre = (p + z * z / (2 * n)) / denom
    margin = z * math.sqrt(p * (1 - p) / n + z * z / (4 * n * n)) / denom
    return (round(max(0, centre - margin) * 100, 1),
            round(min(1, centre + margin) * 100, 1))

def max_drawdown(pls):
    cum = peak = dd = 0
    for pl in pls:
        cum += pl
        peak = max(peak, cum)
        dd = max(dd, peak - cum)
    return round(dd, 2)

def sharpe_ratio(pls):
    if len(pls) < 2:
        return 0.0
    mean_pl = sum(pls) / len(pls)
    var = sum((p - mean_pl) ** 2 for p in pls) / (len(pls) - 1)
    std = math.sqrt(var) if var > 0 else 0.001
    return round(mean_pl / std * math.sqrt(len(pls)), 2)

def pl_back(odds, won):
    return round(STAKE * (odds - 1) * 0.95, 2) if won else -STAKE

def run_backtest(matches, params):
    """Run backtest for a single param combo. Returns list of bets."""
    min_min = params["minute_min"]
    min_max = params["minute_max"]
    sot_adv = params["sot_advantage"]
    score_filter = params["score_filter"]
    odds_lo = params["odds_lo"]
    odds_hi = params["odds_hi"]
    # sot_side: "home" means SoT_home >= SoT_away + adv
    #           "away" means SoT_away >= SoT_home + adv
    #           "any"  means either side dominates
    sot_side = params["sot_side"]

    bets = []
    for match in matches:
        triggered = False
        for idx, row in enumerate(match["rows"]):
            if triggered:
                break
            m = _f(row.get("minuto", ""))
            if m is None or not (min_min <= m <= min_max):
                continue

            gl = _i(row.get("goles_local", ""))
            gv = _i(row.get("goles_visitante", ""))
            if gl is None or gv is None:
                continue
            total_now = gl + gv

            # Anti-tautology: Under 3.5 already dead if >= 4 goals
            if total_now >= 4:
                continue

            # Score filter
            if score_filter == "max_goals_2" and total_now > 2:
                continue
            if score_filter == "max_goals_3" and total_now > 3:
                continue
            # "any" = no score filter (except anti-tautology above)

            # SoT check
            sot_h = _f(row.get("tiros_puerta_local", ""))
            sot_a = _f(row.get("tiros_puerta_visitante", ""))
            if sot_h is None or sot_a is None:
                continue

            sot_ok = False
            if sot_side == "home":
                sot_ok = sot_h >= sot_a + sot_adv
            elif sot_side == "away":
                sot_ok = sot_a >= sot_h + sot_adv
            elif sot_side == "any":
                sot_ok = (sot_h >= sot_a + sot_adv) or (sot_a >= sot_h + sot_adv)

            if not sot_ok:
                continue

            # Odds
            odds = _f(row.get("back_under35", ""))
            if odds is None or odds <= 1.0:
                continue
            if not (odds_lo <= odds <= odds_hi):
                continue

            # Trigger! Use next row for entry (min_dur=1 means same row)
            won = match["ft_total"] <= 3
            bet_pl = pl_back(odds, won)

            bets.append({
                "match_id": match["match_id"],
                "file": match["file"],
                "league": match["league"],
                "country": match["country"],
                "minuto": m,
                "score_trigger": f"{gl}-{gv}",
                "sot_h": int(sot_h),
                "sot_a": int(sot_a),
                "odds": odds,
                "ft": f"{match['ft_local']}-{match['ft_visitante']}",
                "ft_total": match["ft_total"],
                "won": won,
                "pl": bet_pl,
                "timestamp": match["timestamp_first"],
                "bet_type": "back",
            })
            triggered = True

    return bets

def stats(bets):
    if not bets:
        return {"n": 0}
    n = len(bets)
    wins = sum(1 for b in bets if b["won"])
    total_pl = sum(b["pl"] for b in bets)
    avg_odds = sum(b["odds"] for b in bets) / n
    roi = total_pl / (n * STAKE) * 100
    ci_lo, ci_hi = wilson_ci95(n, wins)
    pls = [b["pl"] for b in bets]
    leagues = set(b["league"] for b in bets)

    # Train/test split
    sorted_b = sorted(bets, key=lambda b: b["timestamp"])
    split = int(len(sorted_b) * 0.7)
    train, test = sorted_b[:split], sorted_b[split:]
    train_pl = sum(b["pl"] for b in train)
    test_pl = sum(b["pl"] for b in test)
    train_roi = round(train_pl / (len(train) * STAKE) * 100, 1) if train else 0
    test_roi = round(test_pl / (len(test) * STAKE) * 100, 1) if test else 0

    return {
        "n": n, "wins": wins,
        "wr_pct": round(wins / n * 100, 1),
        "avg_odds": round(avg_odds, 2),
        "roi_pct": round(roi, 1),
        "total_pl": round(total_pl, 2),
        "ci95_lo": ci_lo, "ci95_hi": ci_hi,
        "max_dd": max_drawdown(pls),
        "sharpe": sharpe_ratio(pls),
        "leagues": len(leagues),
        "league_list": sorted(leagues),
        "train_roi": train_roi,
        "test_roi": test_roi,
        "n_train": len(train),
        "n_test": len(test),
    }


def main():
    print("Loading matches...")
    matches = load_matches()
    n_matches = len(matches)
    print(f"Loaded {n_matches} finished matches")
    min_n = max(15, n_matches // 25)
    print(f"Quality gate N >= {min_n}")

    # Parameter grid
    param_grid = {
        "minute_min": [55, 60, 65],
        "minute_max": [75, 80, 85],
        "sot_advantage": [1, 2, 3],
        "score_filter": ["any", "max_goals_2", "max_goals_3"],
        "odds_lo": [1.05, 1.1, 1.2],
        "odds_hi": [3.0, 4.0, 5.0],
        "sot_side": ["home", "away", "any"],
    }

    keys = list(param_grid.keys())
    combos = list(itertools.product(*[param_grid[k] for k in keys]))
    print(f"Grid: {len(combos)} combinations")

    results = []
    best = None
    best_sharpe = -999

    for combo in combos:
        params = dict(zip(keys, combo))
        bets = run_backtest(matches, params)
        if not bets:
            continue
        s = stats(bets)
        if s["n"] < min_n:
            continue
        if s["roi_pct"] < 15:
            continue
        if s["ci95_lo"] < 40:
            continue
        if s["train_roi"] <= 0 or s["test_roi"] <= 0:
            continue
        if s["leagues"] < 3:
            continue

        results.append({"params": params, "stats": s})
        if s["sharpe"] > best_sharpe:
            best_sharpe = s["sharpe"]
            best = {"params": params, "stats": s, "bets": bets}

    print(f"\n{'='*70}")
    print(f"RESULTS: {len(results)} combos pass all quality gates (of {len(combos)} tested)")
    print(f"{'='*70}")

    if not results:
        print("NO combos pass all quality gates.")
        # Show top 10 by N for debugging
        print("\nTop 10 combos by N (for debugging):")
        debug_results = []
        for combo in combos:
            params = dict(zip(keys, combo))
            bets = run_backtest(matches, params)
            if bets:
                s = stats(bets)
                debug_results.append({"params": params, "stats": s})
        debug_results.sort(key=lambda x: x["stats"].get("n", 0), reverse=True)
        for i, r in enumerate(debug_results[:10]):
            p = r["params"]
            s = r["stats"]
            print(f"  {i+1}. N={s['n']}, WR={s['wr_pct']}%, ROI={s['roi_pct']}%, "
                  f"Sharpe={s['sharpe']}, CI95=[{s['ci95_lo']},{s['ci95_hi']}], "
                  f"Train={s['train_roi']}%, Test={s['test_roi']}%, "
                  f"Leagues={s['leagues']}")
            print(f"     Params: min={p['minute_min']}-{p['minute_max']}, sot_adv={p['sot_advantage']}, "
                  f"side={p['sot_side']}, score={p['score_filter']}, odds=[{p['odds_lo']},{p['odds_hi']}]")
        return

    # Sort by Sharpe
    results.sort(key=lambda x: x["stats"]["sharpe"], reverse=True)

    print(f"\nTop 10 passing combos (by Sharpe):")
    for i, r in enumerate(results[:10]):
        p = r["params"]
        s = r["stats"]
        print(f"  {i+1}. N={s['n']}, WR={s['wr_pct']}%, ROI={s['roi_pct']}%, "
              f"Sharpe={s['sharpe']}, CI95=[{s['ci95_lo']},{s['ci95_hi']}], "
              f"AvgOdds={s['avg_odds']}, MaxDD={s['max_dd']}")
        print(f"     Train: N={s['n_train']}, ROI={s['train_roi']}% | "
              f"Test: N={s['n_test']}, ROI={s['test_roi']}%")
        print(f"     Leagues: {s['leagues']} | Params: min={p['minute_min']}-{p['minute_max']}, "
              f"sot_adv={p['sot_advantage']}, side={p['sot_side']}, "
              f"score={p['score_filter']}, odds=[{p['odds_lo']},{p['odds_hi']}]")

    # Export best combo bets
    if best:
        print(f"\n{'='*70}")
        print(f"BEST COMBO (Sharpe={best_sharpe}):")
        print(f"{'='*70}")
        p = best["params"]
        s = best["stats"]
        print(f"  Params: min={p['minute_min']}-{p['minute_max']}, sot_adv={p['sot_advantage']}, "
              f"side={p['sot_side']}, score={p['score_filter']}, odds=[{p['odds_lo']},{p['odds_hi']}]")
        print(f"  N={s['n']}, WR={s['wr_pct']}%, ROI={s['roi_pct']}%, Sharpe={s['sharpe']}")
        print(f"  CI95=[{s['ci95_lo']},{s['ci95_hi']}], AvgOdds={s['avg_odds']}, MaxDD={s['max_dd']}")
        print(f"  Train: N={s['n_train']}, ROI={s['train_roi']}% | Test: N={s['n_test']}, ROI={s['test_roi']}%")
        print(f"  Leagues: {s['leagues']} ({', '.join(s['league_list'][:10])}...)")

        # Score distribution
        score_dist = Counter(b["score_trigger"] for b in best["bets"])
        print(f"\n  Score distribution at trigger:")
        for sc, cnt in score_dist.most_common():
            wins_sc = sum(1 for b in best["bets"] if b["score_trigger"] == sc and b["won"])
            wr_sc = wins_sc / cnt * 100 if cnt else 0
            print(f"    {sc}: {cnt} bets, WR={wr_sc:.1f}%")

        # Export bets JSON
        out_path = os.path.join(os.path.dirname(os.path.abspath(__file__)),
                                "..", "auxiliar", "sd_bt_h103_bets.json")
        with open(out_path, "w", encoding="utf-8") as f:
            json.dump(best["bets"], f, ensure_ascii=False, indent=2)
        print(f"\n  Bets exported to: {out_path}")

        # Also compute overlap with under35_late and under35_3goals
        print(f"\n{'='*70}")
        print("OVERLAP ANALYSIS with existing Under 3.5 strategies")
        print(f"{'='*70}")
        h103_ids = set(b["match_id"] for b in best["bets"])

        # Simulate under35_late: score 1-0/0-1 (goals=1), min 65-75, xG<2.5
        # Actually config says goals_min=3, goals_max=3 now. Let me re-check.
        # Config: under35_late goals_min=3, goals_max=3, min 65-75, xgMax=2.5
        # under35_3goals: exactly 3 goals, min 60-80, xgMax=2.0
        u35_late_ids = set()
        u35_3g_ids = set()
        for match in matches:
            for row in match["rows"]:
                m = _f(row.get("minuto", ""))
                if m is None:
                    continue
                gl = _i(row.get("goles_local", ""))
                gv = _i(row.get("goles_visitante", ""))
                if gl is None or gv is None:
                    continue
                total = gl + gv
                odds = _f(row.get("back_under35", ""))
                if odds is None or odds <= 1.0:
                    continue
                xg_l = _f(row.get("xg_local", "")) or 0
                xg_v = _f(row.get("xg_visitante", "")) or 0
                xg_t = xg_l + xg_v

                # under35_late: goals_min=3, goals_max=3, min 65-75, xgMax 2.5
                if match["match_id"] not in u35_late_ids:
                    if 65 <= m <= 75 and total == 3 and xg_t <= 2.5:
                        u35_late_ids.add(match["match_id"])

                # under35_3goals: exactly 3 goals, min 60-80, xgMax 2.0
                if match["match_id"] not in u35_3g_ids:
                    if 60 <= m <= 80 and total == 3 and xg_t <= 2.0:
                        u35_3g_ids.add(match["match_id"])

        overlap_late = h103_ids & u35_late_ids
        overlap_3g = h103_ids & u35_3g_ids
        print(f"  H103 match IDs: {len(h103_ids)}")
        print(f"  under35_late match IDs: {len(u35_late_ids)}")
        print(f"  under35_3goals match IDs: {len(u35_3g_ids)}")
        print(f"  Overlap H103 vs under35_late: {len(overlap_late)} ({len(overlap_late)/len(h103_ids)*100:.1f}% of H103)")
        print(f"  Overlap H103 vs under35_3goals: {len(overlap_3g)} ({len(overlap_3g)/len(h103_ids)*100:.1f}% of H103)")
        print(f"  SAME MARKET (Under 3.5) → overlap gate applies")

        # Show performance of unique-only bets
        unique_bets = [b for b in best["bets"] if b["match_id"] not in (u35_late_ids | u35_3g_ids)]
        if unique_bets:
            us = stats(unique_bets)
            print(f"\n  Unique bets (not in either existing): N={us['n']}, WR={us['wr_pct']}%, ROI={us['roi_pct']}%")


if __name__ == "__main__":
    main()
