def main():
    print("Loading matches...")
    matches = load_matches()
    n_matches = len(matches)
    print(f"Loaded {n_matches} finished matches")

    min_bets_gate = max(15, n_matches // 25)
    print(f"Min bets gate: N >= {min_bets_gate}")

    # Grid search parameters
    grid = {
        "min_goals_trigger": [3],  # Only 3 — if 4+, Over 3.5 already won
        "minute_min": [40, 45, 50, 55],
        "minute_max": [60, 65, 70, 75],
        "odds_lo": [1.2, 1.5, 1.8],
        "odds_hi": [5.0, 8.0, 12.0],
        "stat_filter": ["none", "xg_high", "goals_per_min_high", "sot_high"],
    }

    keys = list(grid.keys())
    combos = list(itertools.product(*[grid[k] for k in keys]))
    print(f"\nGrid search: {len(combos)} combinations")

    results = []
    best = None

    for i, combo in enumerate(combos):
        params = dict(zip(keys, combo))

        # Skip invalid ranges
        if params["minute_min"] >= params["minute_max"]:
            continue

        bets = run_backtest(matches, params)
        s = compute_stats(bets)

        if s["n"] == 0:
            continue

        # Train/test
        train_bets, test_bets = train_test_split(bets)
        train_s = compute_stats(train_bets)
        test_s = compute_stats(test_bets)

        result = {
            "params": params,
            "n": s["n"],
            "wr_pct": s["wr_pct"],
            "roi_pct": s["roi_pct"],
            "total_pl": s["total_pl"],
            "ci95_lo": s["ci95_lo"],
            "ci95_hi": s["ci95_hi"],
            "avg_odds": s["avg_odds"],
            "max_dd": s["max_dd"],
            "sharpe": s["sharpe"],
            "leagues": s["leagues"],
            "train_n": train_s["n"],
            "train_roi": train_s["roi_pct"],
            "test_n": test_s["n"],
            "test_roi": test_s["roi_pct"],
        }
        results.append(result)

        # Check quality gates
        passes_gates = (
            s["n"] >= min_bets_gate and
            s["roi_pct"] >= 15.0 and  # Raw threshold (need margin for realistic)
            s["ci95_lo"] >= 40.0 and
            train_s["roi_pct"] > 0 and
            test_s["roi_pct"] > 0 and
            s["leagues"] >= 3
        )

        if passes_gates:
            if best is None or s["sharpe"] > best["sharpe"]:
                best = result
                best["bets"] = bets

    # Print results summary
    print(f"\n{'='*80}")
    print(f"GRID SEARCH RESULTS — H102: BACK Over 3.5 Early Goals")
    print(f"{'='*80}")

    # Sort by Sharpe
    results.sort(key=lambda r: r["sharpe"], reverse=True)

    # Show top 20
    print(f"\nTop 20 combos by Sharpe (of {len(results)} valid):")
    print(f"{'Params':<65} {'N':>4} {'WR%':>5} {'ROI%':>6} {'Shp':>5} {'CI_lo':>5} {'trnROI':>6} {'tstROI':>6} {'Lgs':>3}")
    print("-" * 120)
    for r in results[:20]:
        p = r["params"]
        pstr = f"m[{p['minute_min']}-{p['minute_max']}] g>={p['min_goals_trigger']} o[{p['odds_lo']}-{p['odds_hi']}] f={p['stat_filter'][:8]}"
        print(f"{pstr:<65} {r['n']:>4} {r['wr_pct']:>5.1f} {r['roi_pct']:>6.1f} {r['sharpe']:>5.2f} {r['ci95_lo']:>5.1f} {r['train_roi']:>6.1f} {r['test_roi']:>6.1f} {r['leagues']:>3}")

    # Count how many pass gates
    passing = [r for r in results if (
        r["n"] >= min_bets_gate and
        r["roi_pct"] >= 15.0 and
        r["ci95_lo"] >= 40.0 and
        r["train_roi"] > 0 and
        r["test_roi"] > 0 and
        r["leagues"] >= 3
    )]
    print(f"\n{len(passing)} / {len(results)} combos pass all quality gates (raw)")

    # Print passing combos
    if passing:
        passing.sort(key=lambda r: r["sharpe"], reverse=True)
        print(f"\nPASSING COMBOS:")
        print(f"{'Params':<65} {'N':>4} {'WR%':>5} {'ROI%':>6} {'Shp':>5} {'CI_lo':>5} {'trnROI':>6} {'tstROI':>6} {'DD':>6} {'Lgs':>3}")
        print("-" * 130)
        for r in passing:
            p = r["params"]
            pstr = f"m[{p['minute_min']}-{p['minute_max']}] g>={p['min_goals_trigger']} o[{p['odds_lo']}-{p['odds_hi']}] f={p['stat_filter'][:8]}"
            print(f"{pstr:<65} {r['n']:>4} {r['wr_pct']:>5.1f} {r['roi_pct']:>6.1f} {r['sharpe']:>5.2f} {r['ci95_lo']:>5.1f} {r['train_roi']:>6.1f} {r['test_roi']:>6.1f} {r['max_dd']:>6.1f} {r['leagues']:>3}")

    # Best combo
    if best:
        print(f"\n{'='*80}")
        print(f"BEST COMBO (highest Sharpe among passing):")
        print(f"{'='*80}")
        p = best["params"]
        print(f"  Params: minute=[{p['minute_min']}, {p['minute_max']}], min_goals={p['min_goals_trigger']}, "
              f"odds=[{p['odds_lo']}, {p['odds_hi']}], filter={p['stat_filter']}")
        print(f"  N={best['n']}, WR={best['wr_pct']}%, ROI={best['roi_pct']}%, Sharpe={best['sharpe']}")
        print(f"  CI95=[{best['ci95_lo']}%, {best['ci95_hi']}%], AvgOdds={best['avg_odds']}, MaxDD={best['max_dd']}")
        print(f"  Train: N={best['train_n']}, ROI={best['train_roi']}%")
        print(f"  Test: N={best['test_n']}, ROI={best['test_roi']}%")
        print(f"  Leagues: {best['leagues']}")

        # Score distribution
        scores = Counter(b["score_at_trigger"] for b in best["bets"])
        print(f"\n  Score at trigger distribution:")
        for sc, cnt in scores.most_common(10):
            w = sum(1 for b in best["bets"] if b["score_at_trigger"] == sc and b["won"])
            print(f"    {sc}: {cnt} bets, {w} wins ({w/cnt*100:.0f}% WR)")

        # League distribution
        league_counts = Counter(b["league"] for b in best["bets"])
        print(f"\n  Top leagues:")
        for lg, cnt in league_counts.most_common(10):
            w = sum(1 for b in best["bets"] if b["league"] == lg and b["won"])
            print(f"    {lg}: {cnt} bets, {w} wins ({w/cnt*100:.0f}% WR)")

        # Minute distribution
        min_dist = Counter(int(b["minuto"]) for b in best["bets"])
        print(f"\n  Minute distribution:")
        for mn in sorted(min_dist.keys()):
            print(f"    min {mn}: {min_dist[mn]} bets")

        # Market efficiency check: avg odds won vs lost
        won_odds = [b["odds"] for b in best["bets"] if b["won"]]
        lost_odds = [b["odds"] for b in best["bets"] if not b["won"]]
        if won_odds and lost_odds:
            print(f"\n  Market efficiency check:")
            print(f"    Avg odds (won): {sum(won_odds)/len(won_odds):.2f}")
            print(f"    Avg odds (lost): {sum(lost_odds)/len(lost_odds):.2f}")
            print(f"    (Won > Lost = market NOT pricing this pattern = genuine edge)")

        # Export bets for realistic validation
        bets_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "auxiliar", "sd_bt_h102_bets.json")
        with open(bets_path, "w", encoding="utf-8") as f:
            json.dump(best["bets"], f, ensure_ascii=False, indent=2)
        print(f"\n  Bets exported to: {bets_path}")

        # Overlap analysis: find match_ids that would also trigger lay_over45, over25_2goal, etc.
        print(f"\n{'='*80}")
        print(f"OVERLAP ANALYSIS")
        print(f"{'='*80}")
        h102_ids = set(b["match_id"] for b in best["bets"])
        print(f"  H102 match_ids: {len(h102_ids)}")

        # Check what % of H102 matches have 2+ goals (would trigger over25 strategies)
        over25_overlap = sum(1 for b in best["bets"] if int(b["score_at_trigger"].split("-")[0]) + int(b["score_at_trigger"].split("-")[1]) >= 2)
        print(f"  With 2+ goals at trigger (potential over25 overlap): {over25_overlap}/{len(best['bets'])} = {over25_overlap/len(best['bets'])*100:.1f}%")
        print(f"  BUT: Over 2.5 is a DIFFERENT MARKET than Over 3.5 — market overlap = 0%")
        print(f"  No existing strategy trades Over 3.5 — unique market slot")

    else:
        print("\nNo combo passes all quality gates.")
        if results:
            # Show closest
            print("\nClosest combos (sorted by Sharpe):")
            for r in results[:10]:
                p = r["params"]
                fails = []
                if r["n"] < min_bets_gate: fails.append(f"N={r['n']}<{min_bets_gate}")
                if r["roi_pct"] < 15: fails.append(f"ROI={r['roi_pct']}<15")
