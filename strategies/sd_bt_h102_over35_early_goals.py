"""
Backtest: H102 — BACK Over 3.5 with 3+ goals before min 65
Generated by strategy-designer agent, R19

CONCEPT: When 3 goals are already scored early/mid-game (before min 65),
the game has shown high-scoring tendencies. BACK Over 3.5 bets on 1 more
goal arriving. Anti-tautology: if already 4+ goals, Over 3.5 is won — skip.

EDGE THESIS: Market adjusts Over 3.5 odds after goals but anchors on
pre-match xG models. A game with 3 goals by min 55 has demonstrated
attacking intent beyond what models predicted. The 4th goal probability
is higher than what the market prices.

MARKET: BACK Over 3.5 (back_over35 column)
WIN CONDITION: ft_total >= 4
"""
import os, glob, csv, math, json, itertools, sys
from collections import defaultdict, Counter

DATA_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "betfair_scraper", "data")
STAKE = 10.0

def _f(val):
    try:
        v = float(val)
        return v if not math.isnan(v) else None
    except (TypeError, ValueError):
        return None

def _i(val):
    v = _f(val)
    return int(v) if v is not None else None

def wilson_ci95(n, wins):
    if n == 0:
        return (0.0, 0.0)
    z = 1.96
    p = wins / n
    denom = 1 + z * z / n
    centre = (p + z * z / (2 * n)) / denom
    margin = z * math.sqrt(p * (1 - p) / n + z * z / (4 * n * n)) / denom
    return (round(max(0, centre - margin) * 100, 1),
            round(min(1, centre + margin) * 100, 1))

def max_drawdown(pls):
    cum = peak = dd = 0
    for pl in pls:
        cum += pl
        peak = max(peak, cum)
        dd = max(dd, peak - cum)
    return round(dd, 2)

def sharpe_ratio(pls):
    if len(pls) < 2:
        return 0.0
    mean_pl = sum(pls) / len(pls)
    var = sum((p - mean_pl) ** 2 for p in pls) / (len(pls) - 1)
    std = math.sqrt(var) if var > 0 else 0.001
    return round(mean_pl / std * math.sqrt(len(pls)), 2)

def pl_back(odds, won):
    return round(STAKE * (odds - 1) * 0.95, 2) if won else -STAKE

def load_matches():
    pattern = os.path.join(DATA_DIR, "partido_*.csv")
    files = glob.glob(pattern)
    matches = []
    for fpath in files:
        rows = []
        try:
            with open(fpath, encoding="utf-8", errors="replace") as f:
                for row in csv.DictReader(f):
                    rows.append(row)
        except Exception:
            continue
        if len(rows) < 5:
            continue
        last = rows[-1]
        gl = _i(last.get("goles_local", ""))
        gv = _i(last.get("goles_visitante", ""))
        if gl is None or gv is None:
            continue
        matches.append({
            "file": os.path.basename(fpath),
            "match_id": os.path.basename(fpath).replace("partido_", "").replace(".csv", ""),
            "country": last.get("Pais", last.get("País", "?")),
            "league": last.get("Liga", "?"),
            "ft_local": gl,
            "ft_visitante": gv,
            "ft_total": gl + gv,
            "rows": rows,
            "timestamp_first": rows[0].get("timestamp_utc", ""),
        })
    return matches

def run_backtest(matches, params):
    """Run backtest for a single parameter combination."""
    min_min = params["minute_min"]
    min_max = params["minute_max"]
    min_goals = params["min_goals_trigger"]
    odds_lo = params["odds_lo"]
    odds_hi = params["odds_hi"]
    stat_filter = params["stat_filter"]
    min_dur = params.get("min_dur", 1)

    bets = []
    for m in matches:
        rows = m["rows"]
        ft_total = m["ft_total"]
        triggered = False

        for idx, row in enumerate(rows):
            minute = _f(row.get("minuto", ""))
            if minute is None or minute < min_min or minute > min_max:
                continue

            gl = _i(row.get("goles_local", ""))
            gv = _i(row.get("goles_visitante", ""))
            if gl is None or gv is None:
                continue

            current_goals = gl + gv

            # ANTI-TAUTOLOGY: if Over 3.5 already won (4+ goals), skip
            if current_goals > 3:
                continue

            # Must have exactly 3 goals at trigger (or min_goals=3)
            if current_goals < min_goals:
                continue

            # Get odds
            odds = _f(row.get("back_over35", ""))
            if odds is None or odds < odds_lo or odds > odds_hi:
                continue

            # Stat filter
            if stat_filter == "xg_high":
                xg_l = _f(row.get("xg_local", ""))
                xg_v = _f(row.get("xg_visitante", ""))
                if xg_l is None or xg_v is None:
                    continue
                if (xg_l + xg_v) < 2.0:
                    continue
            elif stat_filter == "goals_per_min_high":
                # At least 3 goals / minute > 0.04 goals/min (above average)
                if minute <= 0:
                    continue
                gpm = current_goals / minute
                if gpm < 0.04:
                    continue
            elif stat_filter == "sot_high":
                sot_l = _f(row.get("tiros_puerta_local", ""))
                sot_v = _f(row.get("tiros_puerta_visitante", ""))
                if sot_l is None or sot_v is None:
                    continue
                if (sot_l + sot_v) < 6:
                    continue

            # min_dur: use row min_dur captures later
            entry_idx = min(idx + min_dur - 1, len(rows) - 1)
            entry_row = rows[entry_idx]
            entry_odds = _f(entry_row.get("back_over35", ""))
            if entry_odds is None or entry_odds < odds_lo or entry_odds > odds_hi:
                # Fall back to trigger odds
                entry_odds = odds

            won = ft_total >= 4
            bet = {
                "match_id": m["match_id"],
                "file": m["file"],
                "league": m["league"],
                "country": m["country"],
                "minuto": minute,
                "score_at_trigger": f"{gl}-{gv}",
                "ft_score": f"{m['ft_local']}-{m['ft_visitante']}",
                "odds": entry_odds,
                "won": won,
                "pl": pl_back(entry_odds, won),
                "timestamp": row.get("timestamp_utc", m["timestamp_first"]),
                "bet_type": "back",
            }
            bets.append(bet)
            triggered = True
            break  # 1 bet per match

    return bets

def compute_stats(bets):
    if not bets:
        return {"n": 0, "wins": 0, "wr_pct": 0, "roi_pct": 0, "total_pl": 0,
                "ci95_lo": 0, "ci95_hi": 0, "max_dd": 0, "sharpe": 0,
                "avg_odds": 0, "leagues": 0, "league_list": []}
    n = len(bets)
    wins = sum(1 for b in bets if b["won"])
    wr = wins / n
    total_pl = sum(b["pl"] for b in bets)
    avg_odds = sum(b["odds"] for b in bets) / n
    roi = total_pl / (n * STAKE) * 100
    ci_lo, ci_hi = wilson_ci95(n, wins)
    pls = [b["pl"] for b in bets]
    leagues = set(b.get("league", "?") for b in bets)
    return {
        "n": n, "wins": wins,
        "wr_pct": round(wr * 100, 1),
        "avg_odds": round(avg_odds, 2),
        "roi_pct": round(roi, 1),
        "total_pl": round(total_pl, 2),
        "ci95_lo": ci_lo, "ci95_hi": ci_hi,
        "max_dd": max_drawdown(pls),
        "sharpe": sharpe_ratio(pls),
        "leagues": len(leagues),
        "league_list": sorted(leagues),
    }

def train_test_split(bets, ratio=0.7):
    sorted_b = sorted(bets, key=lambda b: b.get("timestamp", ""))
    split = int(len(sorted_b) * ratio)
    return sorted_b[:split], sorted_b[split:]

