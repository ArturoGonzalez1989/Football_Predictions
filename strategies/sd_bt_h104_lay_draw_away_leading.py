"""
Backtest: H104 — LAY Draw when Away Team Leading by 1 + Low xG
Generated by strategy-designer agent, R20

Concept: When the away team leads by exactly 1 goal and total xG is low,
the draw is unlikely because:
1. Low xG means the match lacks attacking quality — goals are scarce
2. The away team already has the lead and can park the bus
3. The home team lacks the attacking output (low xG) to equalize
4. The market overprices draw probability in these low-quality matches

Anti-tautology: Only valid when away leads by EXACTLY 1 (0-1, 1-2, 2-3).
If away leads by 2+, draw is already very unlikely (guaranteed easy LAY win = tautology).

Win condition for LAY Draw: match ends NOT as a draw (home wins OR away wins).
P/L for LAY: win = STAKE * 0.95, loss = -STAKE * (lay_odds - 1)
"""
import os, glob, csv, math, json, itertools
from collections import defaultdict, Counter

DATA_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "betfair_scraper", "data")
STAKE = 10.0

def _f(val):
    try:
        v = float(val)
        return v if not math.isnan(v) else None
    except (TypeError, ValueError):
        return None

def _i(val):
    v = _f(val)
    return int(v) if v is not None else None

def load_matches():
    pattern = os.path.join(DATA_DIR, "partido_*.csv")
    files = glob.glob(pattern)
    matches = []
    for fpath in files:
        rows = []
        try:
            with open(fpath, encoding="utf-8", errors="replace") as f:
                for row in csv.DictReader(f):
                    rows.append(row)
        except Exception:
            continue
        if len(rows) < 5:
            continue
        last = rows[-1]
        gl = _i(last.get("goles_local", ""))
        gv = _i(last.get("goles_visitante", ""))
        if gl is None or gv is None:
            continue
        matches.append({
            "file": os.path.basename(fpath),
            "match_id": os.path.basename(fpath).replace("partido_", "").replace(".csv", ""),
            "country": last.get("Pais", last.get("País", "?")),
            "league": last.get("Liga", "?"),
            "ft_local": gl,
            "ft_visitante": gv,
            "ft_total": gl + gv,
            "rows": rows,
            "timestamp_first": rows[0].get("timestamp_utc", ""),
        })
    return matches

def wilson_ci95(n, wins):
    if n == 0:
        return (0.0, 0.0)
    z = 1.96
    p = wins / n
    denom = 1 + z * z / n
    centre = (p + z * z / (2 * n)) / denom
    margin = z * math.sqrt(p * (1 - p) / n + z * z / (4 * n * n)) / denom
    return (round(max(0, centre - margin) * 100, 1),
            round(min(1, centre + margin) * 100, 1))

def max_drawdown(pls):
    cum = peak = dd = 0
    for pl in pls:
        cum += pl
        peak = max(peak, cum)
        dd = max(dd, peak - cum)
    return round(dd, 2)

def sharpe_ratio(pls):
    if len(pls) < 2:
        return 0.0
    mean_pl = sum(pls) / len(pls)
    var = sum((p - mean_pl) ** 2 for p in pls) / (len(pls) - 1)
    std = math.sqrt(var) if var > 0 else 0.001
    return round(mean_pl / std * math.sqrt(len(pls)), 2)

def pl_lay(odds, won):
    """LAY P/L: win if event does NOT happen. won=True means draw did NOT happen."""
    if won:
        return round(STAKE * 0.95, 2)  # collect stake minus commission
    else:
        return round(-STAKE * (odds - 1), 2)  # pay out (odds-1) * stake

def run_backtest(matches, params):
    min_min = params["minute_min"]
    min_max = params["minute_max"]
    xg_max = params["xg_max"]
    odds_lo = params["odds_lo"]
    odds_hi = params["odds_hi"]
    score_filter = params["score_filter"]  # "away_plus1_any", "0-1_only", "01_12"

    bets = []
    for match in matches:
        triggered = False
        for idx, row in enumerate(match["rows"]):
            if triggered:
                break
            m = _f(row.get("minuto", ""))
            if m is None or not (min_min <= m <= min_max):
                continue

            gl = _i(row.get("goles_local", ""))
            gv = _i(row.get("goles_visitante", ""))
            if gl is None or gv is None:
                continue

            # Away must lead by EXACTLY 1 (anti-tautology: not 2+)
            if gv - gl != 1:
                continue

            # Score filter
            if score_filter == "0-1_only" and not (gl == 0 and gv == 1):
                continue
            if score_filter == "01_12" and not ((gl == 0 and gv == 1) or (gl == 1 and gv == 2)):
                continue
            # "away_plus1_any" = any score where away leads by 1

            # xG check (low xG total)
            xg_l = _f(row.get("xg_local", ""))
            xg_v = _f(row.get("xg_visitante", ""))
            if xg_l is None or xg_v is None:
                continue
            xg_total = xg_l + xg_v
            if xg_total > xg_max:
                continue

            # LAY Draw odds
            odds = _f(row.get("lay_draw", ""))
            if odds is None or odds <= 1.0:
                continue
            if not (odds_lo <= odds <= odds_hi):
                continue

            # Trigger! LAY Draw wins if final result is NOT a draw
            won = match["ft_local"] != match["ft_visitante"]
            bet_pl = pl_lay(odds, won)

            bets.append({
                "match_id": match["match_id"],
                "file": match["file"],
                "league": match["league"],
                "country": match["country"],
                "minuto": m,
                "score_trigger": f"{gl}-{gv}",
                "xg_total": round(xg_total, 2),
                "odds": odds,
                "ft": f"{match['ft_local']}-{match['ft_visitante']}",
                "ft_total": match["ft_total"],
                "won": won,
                "pl": bet_pl,
                "timestamp": match["timestamp_first"],
                "bet_type": "lay",
            })
            triggered = True

    return bets

def stats(bets):
    if not bets:
        return {"n": 0}
    n = len(bets)
    wins = sum(1 for b in bets if b["won"])
    total_pl = sum(b["pl"] for b in bets)
    avg_odds = sum(b["odds"] for b in bets) / n
    roi = total_pl / (n * STAKE) * 100
    ci_lo, ci_hi = wilson_ci95(n, wins)
    pls = [b["pl"] for b in bets]
    leagues = set(b["league"] for b in bets)

    # Train/test split
    sorted_b = sorted(bets, key=lambda b: b["timestamp"])
    split = int(len(sorted_b) * 0.7)
    train, test = sorted_b[:split], sorted_b[split:]
    train_pl = sum(b["pl"] for b in train)
    test_pl = sum(b["pl"] for b in test)
    train_roi = round(train_pl / (len(train) * STAKE) * 100, 1) if train else 0
    test_roi = round(test_pl / (len(test) * STAKE) * 100, 1) if test else 0

    return {
        "n": n, "wins": wins,
        "wr_pct": round(wins / n * 100, 1),
        "avg_odds": round(avg_odds, 2),
        "roi_pct": round(roi, 1),
        "total_pl": round(total_pl, 2),
        "ci95_lo": ci_lo, "ci95_hi": ci_hi,
        "max_dd": max_drawdown(pls),
        "sharpe": sharpe_ratio(pls),
        "leagues": len(leagues),
        "league_list": sorted(leagues),
        "train_roi": train_roi,
        "test_roi": test_roi,
        "n_train": len(train),
        "n_test": len(test),
    }


def main():
    print("Loading matches...")
    matches = load_matches()
    n_matches = len(matches)
    print(f"Loaded {n_matches} finished matches")
    min_n = max(15, n_matches // 25)
    print(f"Quality gate N >= {min_n}")

    # Parameter grid
    param_grid = {
        "minute_min": [45, 50, 55],
        "minute_max": [70, 75, 80],
        "xg_max": [1.2, 1.5, 1.8, 2.0],
        "odds_lo": [2.0, 2.5, 3.0],
        "odds_hi": [6.0, 8.0, 10.0],
        "score_filter": ["away_plus1_any", "0-1_only", "01_12"],
    }

    keys = list(param_grid.keys())
    combos = list(itertools.product(*[param_grid[k] for k in keys]))
    print(f"Grid: {len(combos)} combinations")

    results = []
    best = None
    best_sharpe = -999

    for combo in combos:
        params = dict(zip(keys, combo))
        bets = run_backtest(matches, params)
        if not bets:
            continue
        s = stats(bets)
        if s["n"] < min_n:
            continue
        if s["roi_pct"] < 10:
            continue
        if s["ci95_lo"] < 40:
            continue
        if s["train_roi"] <= 0 or s["test_roi"] <= 0:
            continue
        if s["leagues"] < 3:
            continue

        results.append({"params": params, "stats": s})
        if s["sharpe"] > best_sharpe:
            best_sharpe = s["sharpe"]
            best = {"params": params, "stats": s, "bets": bets}

    print(f"\n{'='*70}")
    print(f"RESULTS: {len(results)} combos pass all quality gates (of {len(combos)} tested)")
    print(f"{'='*70}")

    if not results:
        print("NO combos pass all quality gates.")
        # Show top 10 by N for debugging
        print("\nTop 10 combos by N (for debugging):")
        debug_results = []
        for combo in combos:
            params = dict(zip(keys, combo))
            bets = run_backtest(matches, params)
            if bets:
                s = stats(bets)
                debug_results.append({"params": params, "stats": s})
        debug_results.sort(key=lambda x: x["stats"].get("n", 0), reverse=True)
        for i, r in enumerate(debug_results[:15]):
            p = r["params"]
            s = r["stats"]
            print(f"  {i+1}. N={s['n']}, WR={s['wr_pct']}%, ROI={s['roi_pct']}%, "
                  f"Sharpe={s['sharpe']}, CI95=[{s['ci95_lo']},{s['ci95_hi']}], "
                  f"Train={s['train_roi']}%, Test={s['test_roi']}%, "
                  f"Leagues={s['leagues']}")
            print(f"     Params: min={p['minute_min']}-{p['minute_max']}, xg_max={p['xg_max']}, "
                  f"score={p['score_filter']}, odds=[{p['odds_lo']},{p['odds_hi']}]")

        # Also show a broader analysis: how many matches have away+1 at different minutes
        print(f"\n{'='*70}")
        print("DIAGNOSTIC: Away+1 frequency analysis")
        print(f"{'='*70}")
        away_plus1_minutes = []
        away_plus1_draws = 0
        away_plus1_total = 0
        for match in matches:
            for row in match["rows"]:
                m = _f(row.get("minuto", ""))
                if m is None:
                    continue
                gl = _i(row.get("goles_local", ""))
                gv = _i(row.get("goles_visitante", ""))
                if gl is None or gv is None:
                    continue
                if gv - gl == 1:
                    away_plus1_minutes.append(m)
                    break  # first occurrence only

        # How many away+1 end as draw vs not
        for match in matches:
            saw_away_plus1 = False
            for row in match["rows"]:
                m = _f(row.get("minuto", ""))
                if m is None:
                    continue
                gl = _i(row.get("goles_local", ""))
                gv = _i(row.get("goles_visitante", ""))
                if gl is None or gv is None:
                    continue
                if gv - gl == 1 and 45 <= m <= 80:
                    saw_away_plus1 = True
                    break
            if saw_away_plus1:
                away_plus1_total += 1
                if match["ft_local"] == match["ft_visitante"]:
                    away_plus1_draws += 1

        draw_rate = away_plus1_draws / away_plus1_total * 100 if away_plus1_total else 0
        print(f"  Matches with away+1 at min 45-80: {away_plus1_total}")
        print(f"  Of those that end as draw: {away_plus1_draws} ({draw_rate:.1f}%)")
        print(f"  LAY Draw WR would be: {100-draw_rate:.1f}%")

        return

    # Sort by Sharpe
    results.sort(key=lambda x: x["stats"]["sharpe"], reverse=True)

    print(f"\nTop 10 passing combos (by Sharpe):")
    for i, r in enumerate(results[:10]):
        p = r["params"]
        s = r["stats"]
        print(f"  {i+1}. N={s['n']}, WR={s['wr_pct']}%, ROI={s['roi_pct']}%, "
              f"Sharpe={s['sharpe']}, CI95=[{s['ci95_lo']},{s['ci95_hi']}], "
              f"AvgOdds={s['avg_odds']}, MaxDD={s['max_dd']}")
        print(f"     Train: N={s['n_train']}, ROI={s['train_roi']}% | "
              f"Test: N={s['n_test']}, ROI={s['test_roi']}%")
        print(f"     Leagues: {s['leagues']} | Params: min={p['minute_min']}-{p['minute_max']}, "
              f"xg_max={p['xg_max']}, score={p['score_filter']}, odds=[{p['odds_lo']},{p['odds_hi']}]")

    # Export best combo bets
    if best:
        print(f"\n{'='*70}")
        print(f"BEST COMBO (Sharpe={best_sharpe}):")
        print(f"{'='*70}")
        p = best["params"]
        s = best["stats"]
        print(f"  Params: min={p['minute_min']}-{p['minute_max']}, xg_max={p['xg_max']}, "
              f"score={p['score_filter']}, odds=[{p['odds_lo']},{p['odds_hi']}]")
        print(f"  N={s['n']}, WR={s['wr_pct']}%, ROI={s['roi_pct']}%, Sharpe={s['sharpe']}")
        print(f"  CI95=[{s['ci95_lo']},{s['ci95_hi']}], AvgOdds={s['avg_odds']}, MaxDD={s['max_dd']}")
        print(f"  Train: N={s['n_train']}, ROI={s['train_roi']}% | Test: N={s['n_test']}, ROI={s['test_roi']}%")
        print(f"  Leagues: {s['leagues']} ({', '.join(s['league_list'][:10])}...)")

        # Score distribution
        score_dist = Counter(b["score_trigger"] for b in best["bets"])
        print(f"\n  Score distribution at trigger:")
        for sc, cnt in score_dist.most_common():
            wins_sc = sum(1 for b in best["bets"] if b["score_trigger"] == sc and b["won"])
            wr_sc = wins_sc / cnt * 100 if cnt else 0
            print(f"    {sc}: {cnt} bets, WR={wr_sc:.1f}%")

        # FT distribution
        ft_dist = Counter(b["ft"] for b in best["bets"])
        print(f"\n  Final score distribution:")
        for ft, cnt in ft_dist.most_common():
            print(f"    {ft}: {cnt} ({cnt/len(best['bets'])*100:.1f}%)")

        # xG stats
        xg_vals = [b["xg_total"] for b in best["bets"]]
        print(f"\n  xG total at trigger: min={min(xg_vals):.2f}, max={max(xg_vals):.2f}, "
              f"mean={sum(xg_vals)/len(xg_vals):.2f}")

        # Export bets JSON
        out_path = os.path.join(os.path.dirname(os.path.abspath(__file__)),
                                "..", "auxiliar", "sd_bt_h104_bets.json")
        with open(out_path, "w", encoding="utf-8") as f:
            json.dump(best["bets"], f, ensure_ascii=False, indent=2)
        print(f"\n  Bets exported to: {out_path}")

        # Overlap analysis with draw strategies (same market group: draw)
        print(f"\n{'='*70}")
        print("OVERLAP ANALYSIS with existing Draw strategies")
        print(f"{'='*70}")
        h104_ids = set(b["match_id"] for b in best["bets"])

        # Simulate draw_11: score 1-1, min 70+
        draw_11_ids = set()
        # Simulate draw_xg_conv: score 1-1, xG convergent, min 70+
        draw_xg_conv_ids = set()
        # Simulate draw_22 (H68): score 2-2, min 65+
        draw_22_ids = set()
        # Simulate draw_equalizer (H62): underdog equalized, min 55+
        draw_eq_ids = set()

        for match in matches:
            for row in match["rows"]:
                m = _f(row.get("minuto", ""))
                if m is None:
                    continue
                gl = _i(row.get("goles_local", ""))
                gv = _i(row.get("goles_visitante", ""))
                if gl is None or gv is None:
                    continue

                # draw_11: 1-1 at min 70+
                if match["match_id"] not in draw_11_ids:
                    if gl == 1 and gv == 1 and m >= 70:
                        draw_11_ids.add(match["match_id"])

                # draw_xg_conv: 1-1 at min 70+ (simplified)
                if match["match_id"] not in draw_xg_conv_ids:
                    if gl == 1 and gv == 1 and m >= 70:
                        draw_xg_conv_ids.add(match["match_id"])

                # draw 2-2 (H68)
                if match["match_id"] not in draw_22_ids:
                    if gl == 2 and gv == 2 and m >= 65:
                        draw_22_ids.add(match["match_id"])

                # draw equalizer (H62) — simplified: tied after being behind
                if match["match_id"] not in draw_eq_ids:
                    if gl == gv and m >= 55:
                        draw_eq_ids.add(match["match_id"])

        # H104 triggers when away leads by 1 — score is NOT tied
        # So by definition, H104 cannot overlap with draw_11, draw_xg_conv, draw_22
        # because those trigger when score IS tied.
        # However, check for match-level overlap (same match, different trigger moments)
        overlap_d11 = h104_ids & draw_11_ids
        overlap_dxgc = h104_ids & draw_xg_conv_ids
        overlap_d22 = h104_ids & draw_22_ids
        overlap_deq = h104_ids & draw_eq_ids

        print(f"  H104 match IDs: {len(h104_ids)}")
        print(f"  draw_11 match IDs: {len(draw_11_ids)}")
        print(f"  draw_xg_conv match IDs: {len(draw_xg_conv_ids)}")
        print(f"  draw_22 (H68) match IDs: {len(draw_22_ids)}")
        print(f"  draw_equalizer (H62) match IDs: {len(draw_eq_ids)}")
        print(f"\n  Overlap H104 vs draw_11: {len(overlap_d11)} ({len(overlap_d11)/len(h104_ids)*100:.1f}% of H104)")
        print(f"  Overlap H104 vs draw_xg_conv: {len(overlap_dxgc)} ({len(overlap_dxgc)/len(h104_ids)*100:.1f}% of H104)")
        print(f"  Overlap H104 vs draw_22: {len(overlap_d22)} ({len(overlap_d22)/len(h104_ids)*100:.1f}% of H104)")
        print(f"  Overlap H104 vs draw_eq: {len(overlap_deq)} ({len(overlap_deq)/len(h104_ids)*100:.1f}% of H104)")
        print(f"\n  NOTE: H104 is LAY Draw (away+1 score, NOT tied).")
        print(f"  Existing draw strategies are BACK Draw (tied score).")
        print(f"  Same MATCH can appear in both if score changes during game,")
        print(f"  but they are OPPOSITE SIDES of the draw market.")
        print(f"  The dedup question: LAY Draw and BACK Draw are different DIRECTIONS.")
        print(f"  In _STRATEGY_MARKET, the 'draw' group currently contains BACK Draw strategies.")
        print(f"  LAY Draw could be a separate market group or needs special handling.")

        # Robustness: show results for ALL combos that pass gates (not just best)
        print(f"\n{'='*70}")
        print(f"ROBUSTNESS: {len(results)} of {len(combos)} combos pass all gates")
        print(f"Pass rate: {len(results)/len(combos)*100:.1f}%")
        print(f"{'='*70}")

        # Show parameter sensitivity
        for param_name in keys:
            print(f"\n  Parameter '{param_name}' sensitivity:")
            val_counts = defaultdict(list)
            for r in results:
                val_counts[r["params"][param_name]].append(r["stats"]["roi_pct"])
            for val in sorted(val_counts.keys(), key=str):
                rois = val_counts[val]
                print(f"    {val}: {len(rois)} combos pass, avg ROI={sum(rois)/len(rois):.1f}%")


if __name__ == "__main__":
    main()
