        trigger_scores = Counter(b["score"] for b in bets)
        print(f"\n--- Score at trigger ---")
        for score, count in trigger_scores.most_common():
            print(f"  {score}: {count} ({count/len(bets)*100:.1f}%)")

        # Final score distribution
        ft_scores = Counter(b["ft_score"] for b in bets)
