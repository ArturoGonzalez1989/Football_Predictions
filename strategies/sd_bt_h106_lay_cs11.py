"""
Backtest: H106 — LAY Correct Score 1-1 in specific score states
Generated by strategy-designer agent

KEY INSIGHT: LAY CS 1-1 is only valid when the final score 1-1 is still
MATHEMATICALLY REACHABLE. If the current score is already past 1-1 in either
dimension (e.g., 1-2, 2-1, 2-2), CS 1-1 is DEAD and LAY is a tautological win.

Valid score states where 1-1 is still reachable:
  - 0-0: both need to score 1
  - 0-1: home needs 1, away already has 1 (must not score more)
  - 1-0: away needs 1, home already has 1 (must not score more)
  - 1-1: ALREADY 1-1 — very different dynamic (bet on it NOT staying)

Tautological states (1-1 is IMPOSSIBLE, LAY always wins):
  - 2-0, 0-2, 2-1, 1-2, 2-2, 3-0, 0-3, etc.
"""
import os, glob, csv, math, json, sys
from collections import defaultdict, Counter

DATA_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "betfair_scraper", "data")
STAKE = 10.0

def _f(val):
    try:
        v = float(val)
        return v if not math.isnan(v) else None
    except (TypeError, ValueError):
        return None

def _i(val):
    v = _f(val)
    return int(v) if v is not None else None

def is_cs11_reachable(gl, gv):
    """CS 1-1 is reachable only if gl <= 1 AND gv <= 1."""
    return gl <= 1 and gv <= 1

def is_cs11_tautology(gl, gv):
    """CS 1-1 is impossible if gl > 1 OR gv > 1."""
    return gl > 1 or gv > 1

def load_matches():
    pattern = os.path.join(DATA_DIR, "partido_*.csv")
    files = glob.glob(pattern)
    matches = []
    for fpath in files:
        rows = []
        try:
            with open(fpath, encoding="utf-8", errors="replace") as f:
                for row in csv.DictReader(f):
                    rows.append(row)
        except Exception:
            continue
        if len(rows) < 5:
            continue
        last = rows[-1]
        gl = _i(last.get("goles_local", ""))
        gv = _i(last.get("goles_visitante", ""))
        if gl is None or gv is None:
            continue
        matches.append({
            "file": os.path.basename(fpath),
            "match_id": os.path.basename(fpath).replace("partido_", "").replace(".csv", ""),
            "country": last.get("Pais", last.get("País", "?")),
            "league": last.get("Liga", "?"),
            "ft_local": gl,
            "ft_visitante": gv,
            "ft_total": gl + gv,
            "rows": rows,
            "timestamp_first": rows[0].get("timestamp_utc", ""),
        })
    return matches

def wilson_ci95(n, wins):
    if n == 0:
        return (0.0, 0.0)
    z = 1.96
    p = wins / n
    denom = 1 + z * z / n
    centre = (p + z * z / (2 * n)) / denom
    margin = z * math.sqrt(p * (1 - p) / n + z * z / (4 * n * n)) / denom
    return (round(max(0, centre - margin) * 100, 1),
            round(min(1, centre + margin) * 100, 1))

def max_drawdown(pls):
    cumulative = 0
    peak = 0
    max_dd = 0
    for pl in pls:
        cumulative += pl
        peak = max(peak, cumulative)
        dd = peak - cumulative
        max_dd = max(max_dd, dd)
    return round(max_dd, 2)

def sharpe_ratio(pls):
    if len(pls) < 2:
        return 0.0
    mean_pl = sum(pls) / len(pls)
    variance = sum((p - mean_pl) ** 2 for p in pls) / (len(pls) - 1)
    std_pl = math.sqrt(variance) if variance > 0 else 0.001
    return round(mean_pl / std_pl * math.sqrt(len(pls)), 2)

def pl_lay(lay_odds, won):
    """LAY P/L: win = +STAKE * 0.95, loss = -STAKE * (odds - 1)"""
    return round(STAKE * 0.95, 2) if won else round(-(STAKE * (lay_odds - 1)), 2)

def stats(bets, label=""):
    if not bets:
        return {"n": 0, "label": label}
    n = len(bets)
    wins = sum(1 for b in bets if b["won"])
    wr = wins / n
    total_pl = sum(b["pl"] for b in bets)
    avg_odds = sum(b["odds"] for b in bets) / n
    roi = total_pl / (n * STAKE) * 100
    ci_lo, ci_hi = wilson_ci95(n, wins)
    pls = [b["pl"] for b in bets]
    leagues = set(b.get("league", "?") for b in bets)
    return {
        "label": label, "n": n, "wins": wins,
        "wr_pct": round(wr * 100, 1),
        "avg_odds": round(avg_odds, 2),
        "roi_pct": round(roi, 1),
        "total_pl": round(total_pl, 2),
        "ci95_lo": ci_lo, "ci95_hi": ci_hi,
        "max_dd": max_drawdown(pls),
        "sharpe": sharpe_ratio(pls),
        "leagues": len(leagues),
        "league_list": sorted(leagues),
    }

def run_backtest(matches, min_min, max_min, score_filter, odds_min=1.5, odds_max=50.0,
                 xg_filter=None, min_dur=1, label=""):
    """
    score_filter: function(gl, gv) -> bool for current score
    xg_filter: None or max total xG allowed
    """
    bets = []
    tautology_count = 0

    for m in matches:
        triggered = False
        for idx, row in enumerate(m["rows"]):
            if triggered:
                break
            minute = _f(row.get("minuto", ""))
            if minute is None or not (min_min <= minute <= max_min):
                continue

            gl = _i(row.get("goles_local", ""))
            gv = _i(row.get("goles_visitante", ""))
            if gl is None or gv is None:
                continue

            # Check if CS 1-1 is still reachable
            if is_cs11_tautology(gl, gv):
                tautology_count += 1
                continue  # SKIP tautological wins

            # Apply score filter
            if not score_filter(gl, gv):
                continue

            # Get odds
            lay_odds = _f(row.get("lay_rc_1_1", ""))
            if lay_odds is None or lay_odds < odds_min or lay_odds > odds_max:
                continue

            # xG filter
            if xg_filter is not None:
                xg_l = _f(row.get("xg_local", ""))
                xg_v = _f(row.get("xg_visitante", ""))
                if xg_l is not None and xg_v is not None:
                    if (xg_l + xg_v) > xg_filter:
                        continue

            # Apply min_dur
            entry_idx = min(idx + min_dur - 1, len(m["rows"]) - 1)
            entry_row = m["rows"][entry_idx]

            # Win condition: final score is NOT 1-1
            won = not (m["ft_local"] == 1 and m["ft_visitante"] == 1)
            pl = pl_lay(lay_odds, won)

            bets.append({
                "match_id": m["match_id"],
                "file": m["file"],
                "minute": minute,
                "score": f"{gl}-{gv}",
                "ft_score": f"{m['ft_local']}-{m['ft_visitante']}",
                "odds": lay_odds,
                "won": won,
                "pl": pl,
                "league": m["league"],
                "country": m["country"],
                "timestamp": m["timestamp_first"],
            })
            triggered = True

    return bets, tautology_count

def train_test_split(bets, train_ratio=0.7):
    sorted_bets = sorted(bets, key=lambda b: b.get("timestamp", ""))
    split_idx = int(len(sorted_bets) * train_ratio)
    return sorted_bets[:split_idx], sorted_bets[split_idx:]

def print_stats(s, indent=""):
    if s["n"] == 0:
        print(f"{indent}  No bets")
        return
    print(f"{indent}  N={s['n']}, W={s['wins']}, WR={s['wr_pct']}%, "
          f"ROI={s['roi_pct']}%, P/L={s['total_pl']}, "
          f"AvgOdds={s['avg_odds']}, CI95=[{s['ci95_lo']}%, {s['ci95_hi']}%], "
          f"MaxDD={s['max_dd']}, Sharpe={s['sharpe']}, "
          f"Leagues={s['leagues']}")

def main():
    print("=" * 80)
    print("H106: LAY Correct Score 1-1")
    print("=" * 80)

    matches = load_matches()
    n_matches = len(matches)
    print(f"\nLoaded {n_matches} finished matches")
    n_gate = max(15, n_matches // 25)
    print(f"N gate: {n_gate}")

