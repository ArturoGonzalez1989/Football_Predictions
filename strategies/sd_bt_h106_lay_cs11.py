        ts = r["test"]
        print(f"{r['label']:<45} {s['n']:>4} {s['wr_pct']:>6.1f} {s['roi_pct']:>7.1f} "
              f"{s['sharpe']:>6.2f} {s['ci95_lo']:>6.1f} {tr['roi_pct']:>7.1f} {ts['roi_pct']:>7.1f} "
              f"{s['leagues']:>3} {s['avg_odds']:>6.2f}")

